package com.dicoding.kotlin

fun main() {
    var vocal = 'A'

    //increment
    println("Vocal " + vocal++)
    println("Vocal " + vocal++)
    println("Vocal " + vocal++)
    println("Posisi vocal sekarang : $vocal")
    //decrement
    println("Vocal " + vocal--)
    println("Vocal " + vocal--)
    println("Vocal " + vocal--)
    println("Vocal " + vocal--)
}